txt="Hi am gwen ,I am a software engineer"
x=txt.capitalize()  # Capitalizes the first letter of the string
print(x)

y=txt.upper()  # Converts all characters to uppercase
print(y)

a=txt.lower()  # Converts all characters to lowercase
print(a)

b=txt.title()  # Capitalizes the first letter of each word
print(b)    

c=txt.swapcase()  # Swaps the case of each character
print(c)  # Swaps the case of each character

d=txt.casefold()  # Converts the string to a case-insensitive format
print(d)  # Converts the string to a case-insensitive format




